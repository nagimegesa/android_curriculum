package com.xxzz.curriculum.index;

public class BooKInfo {
    private  String name;
    private  String coverPath;

    public BooKInfo(String name, String coverPath) {
        this.name = name;
        this.coverPath = coverPath;
    }
    public String getName() {
        return name;
    }
    public String getCoverPath() {
        return coverPath;
    }
}
